#include "math_util.h"

int sum(int a, int b)
{
    return a+b;
}

int minus(int a, int b)
{
    return a-b;
}

double devide(int a, int b)
{
    return (double)a/b;
}

int multiply(int a, int b)
{
    return a*b;
}
